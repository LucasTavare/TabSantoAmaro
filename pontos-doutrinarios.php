<b>• Por que batizar em Nome de Senhor Jesus Cristo?
Paulo diz:</b>

<br>
<br>

“Assim que já não sois estrangeiros, nem forasteiros, mas concidadãos dos santos, e da família de Deus;<br>

Edificados sobre o fundamento dos apóstolos e dos profetas, de que Jesus Cristo é a principal pedra da esquina;”<br>
<br>
Efésios 2:19-20<br>
<br>
Se somos edificados sobre esse fundamento, não deveríamos pregar o mesmo Evangelho que os apóstolos pregaram, ensinar como eles ensinaram e seguir seus exemplos em relação a toda doutrina da salvação?<br>

Com certeza, pois Paulo disse:<br>

“Mas, ainda que nós mesmos ou um anjo do céu vos anuncie outro evangelho além do que já vos tenho anunciado, seja anátema;”<br>
<br>
Gálatas 1:8<br>
<br>
- Os apóstolos batizaram somente em Nome do Senhor Jesus Cristo<br>
Sob a unção do Espírito Santo, convidamos você a explorar a análise minuciosa dos relatos e atos de batismo praticados pelos apóstolos, todos esses, baseados no fundamento que foi a divina ordenança de Jesus, registrados na Santa Palavra de Deus.<br>

- A ordenança<br>
Primeiro vemos a ordenança que foi dada por Jesus em Mateus 28:19:<br>

“Portanto ide, fazei discípulos de todas as nações, batizando-os em nome do Pai, e do Filho, e do Espírito Santo.”<br>
<br>
Mateus 28:19<br>
<br>
Jesus não indicou aos apóstolos que usassem estas palavras como uma fórmula. Ele ordenou o batismo usando o Nome, por isso os apóstolos não batizaram repetindo as palavras: “Em Nome do Pai, e do Filho e do Espírito Santo.”, eles apenas batizaram em nome do Senhor Jesus Cristo, pois Ele era a própria manifestação do Pai:<br>

“Manifestei o teu nome aos homens que do mundo me deste; eram teus, e tu mós deste...”<br>
<br>
João 17:6<br>
<br>
- Batismo de Judeus<br>
Após Jesus ressuscitar, subir aos céus e o Espírito Santo descer no dia de Pentecostes, descrito em Atos 2:1-8, uma multidão se aproximou e foi convencida de que aquele grupo de 120 pessoas que estavam reunidas no cenáculo haviam recebido algo de Deus e que também necessitavam daquilo.<br>

Embora Mateus estivesse presente quando Pedro se colocou de pé com o demais apóstolos, não há registro de que ele tenha corrigido a Pedro em batizar em nome de Jesus, conforme descrito em Atos 2.<br>

Diante da pergunta: “Que faremos?”, feita por aqueles corações sinceros e famintos, Pedro respondeu:<br>

“Arrependei-vos, e cada um de vós seja batizado em nome de Jesus Cristo, para perdão dos pecados; e recebereis o dom do Espírito Santo;”<br>
<br>
Atos 2:38<br>
<br>
Estava Pedro errado em ter ordenado o batismo no nome de Jesus no dia de Pentecostes?<br>

Há quem diga que essas palavras de Pedro, para que fossem batizados em nome de Jesus, aplica-se somente àquela multidão de judeus e o batismo servia somente para que confessassem a Jesus Cristo.<br>

- Batismo de Samaritanos<br>
No entanto, em Atos 8 vemos a notável jornada de Filipe à Samaria pregando acerca do reino de Deus e do nome de Jesus Cristo. Note que esse povo não era judeu e sim samaritano. Sedentos por aquelas boas novas, receberam a Palavra com atenção sincera e reverente à pregação de Filipe:<br>

“Mas, como cressem em Filipe, que lhes pregava acerca do reino de Deus, e do nome de Jesus Cristo, se batizavam, tanto homens como mulheres.”<br>

“Os apóstolos, pois, que estavam em Jerusalém, ouvindo que Samaria recebera a palavra de Deus, enviaram para lá Pedro e João.<br>

Os quais, tendo descido, oraram por eles para que recebessem o Espírito Santo.<br>

(Porque sobre nenhum deles tinha ainda descido; mas somente eram batizados em nome do Senhor Jesus).<br>

Então lhes impuseram as mãos, e receberam o Espírito Santo.”<br>
<br>
Atos 8:12,14-17<br>
<br>
- Batismo de Gentios<br>
Se Pedro estivesse errado no dia de Pentecostes, certamente haveria tido bastante tempo para se corrigir antes que fosse à casa de Cornélio.<br>

Ao analisarmos Atos 10 quando Pedro chega à casa do centurião chamado Cornélio, o vemos instruindo aos gentios para que fossem batizados no nome do Senhor.<br>

Cornélio era um homem justo e temia a Deus com toda a sua casa. Ele viu em uma visão um anjo que lhe disse para mandar chamar a Pedro em Jope, o qual lhes diria as palavras pelas quais poderiam ser salvos. Quando Pedro declarou a grande verdade, que por meio do nome de Jesus todo aquele que Nele crê recebe remissão de pecados, o Espírito Santo caiu sobre todos que ouviram a Palavra e eles falaram em línguas como fizeram os apóstolos no dia de Pentecostes. Então Pedro respondeu:<br>

“... Pode alguém porventura recusar a água, para que não sejam batizados estes, que também receberam como nós o Espírito Santo?<br>

E mandou que fossem batizados em nome do Senhor. Então rogaram-lhe que ficasse com eles por alguns dias.”<br>

Atos 10:47 e 48<br>

A seguir observaremos um ato batismal dirigido por Paulo:<br>

“E sucedeu que, enquanto Apolo estava em Corinto, Paulo, tendo passado por todas as regiões superiores, chegou a Éfeso; e achando ali alguns discípulos,<br>

Disse-lhes: Recebestes vós já o Espírito Santo quando crestes? E eles disseram-lhe: Nós nem ainda ouvimos que haja Espírito Santo.<br>

Perguntou-lhes, então: Em que sois batizados então? E eles disseram: No batismo de João.<br>

Mas Paulo disse: Certamente João batizou com o batismo de arrependimento, dizendo ao povo que cresse no que após ele havia de vir, isto é, em Jesus Cristo.<br>

E os que ouviram foram batizados em nome do Senhor Jesus.<br>

E, impondo-lhes Paulo as mãos, veio sobre eles o Espírito Santo; e falavam línguas, e profetizavam.”<br>
<br>
Atos 19:1-6<br>
<br>
Não cremos que Paulo tenha alterado a fórmula ou o modo do batismo quando batizou Lídia e sua família, assim como o carcereiro de Filipos. É interessante notar que, embora não estivesse presente quando Jesus deu suas instruções finais aos apóstolos em Mateus 28:19 e Lucas 24:47, Paulo batizava em nome do Senhor Jesus Cristo.<br>

Note também que o Evangelho de Paulo não é uma cópia ou imitação de outros apóstolos, mas é uma revelação. De quem recebeu esta revelação?<br>

“Mas faço-vos saber irmãos, que o Evangelho que por mim foi anunciado não aprendi de homem algum, mas pela revelação de Jesus Cristo.”<br>
<br>
Gálatas 1:11-12<br>
<br>
Ele escreveu:<br>

“E quanto fizerdes por palavras ou obras, fazei tudo em nome do Senhor Jesus Cristo, dando por Ele graças ao Pai.”<br>
<br>
Colossenses 3:17<br>
<br>
Paulo reclamou ter autoridade divina:<br>
<br>
“Se alguém cuida ser profeta, ou espiritual, reconheça que as coisas que vos escrevo são mandamentos do Senhor.”<br>
<br>
I Coríntios 14:37<br>
<br>
O batismo com água é executado tanto por palavras como por obras. Não podemos deixar de comunicar esta ordem à igreja.<br><br>
<br>
“Por causa disto me ponho de joelhos perante o Pai de nosso Senhor Jesus Cristo, do qual toda a família nos céus e na terra toma o nome.”<br>
<br>
Efésios 3:14-15<br>
<br>
- Conclusão<br>
Devemos ler os Atos dos Apóstolos e as epístolas de Paulo para saber o que os apóstolos ensinaram e praticaram após a vinda do Espírito Santo.<br>
<br>
Você dirá:<br>
<br>
Se Jesus quisesse que nos batizássemos em Seu nome, então, por que Ele não nos disse?:<br>

Ele disse. Note que Ele falou: “no nome”.<br>

Qual é o Nome do Pai?<br>
<br>
Jesus dá a resposta em São João 5:43: “Eu vim em nome de meu Pai”.<br>
<br>

E qual é o Nome do Filho?<br>
<br>
A resposta é dada por um anjo em Mateus 1:21: “... e chamará o seu nome Jesus”.<br>
<br>

E em qual nome o Espírito Santo é revelado?<br>
<br>
A resposta é encontrada em João 14:26: “Mas aquele Consolador, o Espírito Santo, que o Pai enviará em meu nome, esse vos ensinará todas as coisas...”<br>
<br>
Em Atos 2, após ter escutado as instruções de Jesus e ter seu entendimento aberto, Pedro inspirado pelo Espírito Santo pregou até que os corações dos ouvintes ficaram comovidos e sentindo-se condenados perguntaram a Pedro e aos demais apóstolos: “Que faremos, homens irmãos?” Pedro então não hesitou e firmemente deu a prescrição do batismo.<br>

Jesus hoje tem que abrir nosso entendimento da mesma forma que Ele abriu o entendimento de Seus discípulos em Lucas 24:45-47:<br>

“Então abriu-lhes o entendimento para compreenderem as Escrituras.<br>

E disse-lhes: Assim está escrito, e assim convinha que o Cristo padecesse, e ao terceiro dia ressuscitasse dentre os mortos<br>

“E em seu nome se pregasse o arrependimento e a remissão dos pecados, em todas as nações, começando por Jerusalém.”<br>
<br>
Lucas 24:47<br>
<br>
Alguns dizem que aceitarão as palavras de Jesus em Mateus 28:19, porém não as de Pedro em Atos 2:38.<br>

Mas foi Pedro quem falou em Atos 2:38, no dia de Pentecostes, ou foi o Espírito Santo?<br>
<br>
Em I Pedro 1:12, Pedro disse que era o Espírito Santo mandado dos céus:<br>
<br>
“Aos quais foi revelado que, não para si mesmos, mas para nós, eles ministravam estas coisas que agora vos foram anunciadas por aqueles que, pelo Espírito Santo enviado do céu, vos pregaram o evangelho; para as quais coisas os anjos desejam bem atentar.”<br>
<br>
I Pedro 1:12<br>
<br>
Pedro foi um apóstolo e a ele foram dadas as chaves do reino:<br>

“E Simão Pedro, respondendo, disse: Tu és o Cristo, o Filho do Deus vivo.<br>

E Jesus, respondendo, disse-lhe: Bem-aventurado és tu, Simão Barjonas, porque tu não revelou a carne e o sangue, mas meu Pai, que está nos céus.”<br>
<br>
Mateus 16:16-17<br>
<br>
Aqui somos animados a considerar as palavras dos santos profetas e o mandamento dos apóstolos, e o apóstolo Pedro ordenou que o batismo fosse em nome do Senhor Jesus Cristo. O senso comum lhe dirá que o livro dos Atos é a Igreja em ação; e se a batizaram, então assim é como se deve batizar.<br>

E não temos o direito de desacreditá-lo em suas palavras:<br>

“Para que lembreis das palavras que primeiramente foram dadas pelos santos profetas, e do mandamento do Senhor e Salvador, mediante vossos apóstolos.”<br>
<br>
II Pedro 3:2<br>
<br>
De coração aberto, consulte as Escrituras, seja batizado no nome do Senhor Jesus Cristo, e Deus lhe encherá com Seu Espírito. A fórmula correta para o batismo nas águas tem sido restaurada nestes últimos dias.<br>

<br>
<b>• Jesus, Senhor e Cristo</b>
<br>
<br>

A doutrina da “Trindade”, inteiramente antibíblica, não é encontrada na era apostólica. A Bíblia não diz nada de uma primeira, segunda ou terceira pessoa. Mas sim, que Deus foi manifestado em carne:<br>

“E sem dúvida alguma grande é o mistério da piedade: Aquele que se manifestou em carne, foi justificado em espírito, visto dos anjos, pregado aos gentios, crido no mundo, e recebido acima na glória.”<br>
<br>
I Timóteo 3:16<br>
<br>
Isso deveria pôr fim ao assunto. Deus veio em forma humana. Ele era Senhor e Cristo, porém o tabernáculo de fora, de carne, que os homens podiam ver foi chamado Jesus.<br>

“Esteja absolutamente certa, pois, toda a casa de Israel, que este Jesus que vós crucificastes, Deus o fez Senhor e Cristo.”<br>
<br>
Atos 2:36<br>
<br>
Podes ver aí? Senhor Jesus Cristo.<br>

Se Jesus é Senhor e Cristo, então reconhecemos que Ele é e não pode ser outra coisa senão Pai, Filho e Espírito Santo, em uma pessoa manifesta em carne. Assim, não se trata de três pessoas, mas sim de um Deus manifesto em três títulos maiores.<br>

Em Apocalipse 1:8 diz claramente que Jesus é “O que é”, “O que era”, “O e que há de vir”. Ele é o “Alfa e o Ômega”, de A até o Z, que significa o princípio e o fim, o Tudo. Ele é o Deus Poderoso. Ele é a Rosa de Saron, o Lírio dos Vales, a Estrela esplandecente da Manhã, a Videira Verdadeira, o Pai, o Filho e o Espírito Santo. Ele é Deus todo, O único Deus.<br>

Atualmente, a verdade sobre a Deidade tem sido tão distorcida que é preciso receber uma revelação para compreender a verdadeira natureza de Deus. Se você buscar essa compreensão com humildade de coração, certamente a obterá. Então, será capaz de ver Jesus como a imagem perfeita de Deus, a semente da mulher concebida pelo próprio Deus.<br>

Deus criou no ventre de Maria tanto o óvulo quanto o germe de vida, sendo Jesus portanto, todo de Deus, sem nenhuma mistura humana, e Maria a incubadora humana que Deus usou.<br>

Observe na Bíblia e veja que Ele nasceu o Cristo, no entanto, qual foi o nome que o anjo anunciou?<br>

“Ora, o nascimento de Jesus Cristo foi assim: Estando Maria, sua mãe, desposada com José, antes de se ajuntarem, achou-se ter concebido do Espírito Santo.”<br>

“E dará à luz um filho e chamarás o seu nome JESUS; porque ele salvará o seu povo dos seus pecados.”<br>

O anjo não disse que Ele chamaria Jesus Cristo, pois o nome que lhe foi dado aqui entre os homens foi Jesus.<br>

Agora, há muitas pessoas hoje no mundo que se chamam “Jesus”, porém somente há um nascido o Cristo.<br>
<br>
Em João 4:24:<br>
<br>
“Deus é Espírito, e importa que os que o adoram o adorem em espírito e em verdade.”<br>
<br>
João 4:24<br>
<br>
O Grande Arquiteto construiu para Si mesmo um Corpo. Ele não teve que pedir emprestado a ninguém. Deus construiu para Si mesmo uma Casa (Jesus) e nela habitou em plenitude:<br>

“Porque nele habita corporalmente toda a plenitude da divindade;”<br>
<br>
Colossenses 2:9<br>
<br>
Agora, em meio a tanta confusão religiosa, ao se deparar com a Verdade, é muito natural que algumas indagações surjam na mente daqueles que realmente anseiam pela verdade. E é sabendo disso que nos propomos a lhe ajudar através deste livrete.<br>
<br>
- Pergunta sobre João 5:17<br>
<br>
Mas o próprio Jesus diz em João 5:17: “...Meu Pai trabalha até agora, e eu trabalho também.”?<br>

Isto não quer dizer duas pessoas. O Pai (Espírito) estava trabalhando através do Filho (Corpo). O Filho é quem estava fazendo a obra do Pai:<br>

“Porque eu desci do céu, não para fazer a minha vontade, mas a vontade daquele que me enviou.”<br>
<br>
João 6:38<br>
<br>
“Eu e o Pai somos um.”<br>
<br>
João 10:30<br>
<br>
Estava ali o Espírito realizando a obra através da carne chamada Jesus. O Espírito Deus em Jesus:<br>

“Deus estava em Cristo reconciliando o mundo consigo mesmo.”<br>
<br>
II Coríntios 5:19<br>
<br>
“No princípio era o Verbo e o Verbo estava com Deus e o Verbo era Deus e nada do que foi feito foi feito sem Ele.”<br>
<br>
João 1:1<br>
<br>
“E o Verbo se fez carne, e habitou entre nós, e vimos a sua glória, como a glória do unigênito do Pai, cheio de graça e de verdade.”<br>
<br>
João 1:14<br>
<br>
- Pergunta sobre João 14:16<br>
<br>

Bom, contudo ainda não posso entender por que Jesus disse em João 14:16: “Eu rogarei ao Pai e Ele vos dará outro consolador.”<br>

Não pare a leitura no verso 16, mas prossiga no verso 17 e 18:<br>

“O Espírito de verdade, que o mundo não pode receber, porque não o vê nem o conhece; mas vós o conheceis, porque habita convosco, e estará em vós.<br>

Não vos deixarei órfãos; voltarei para vós.”<br>
<br>
João 14:17-18<br>
<br>
Ele estava ali encarnado em Jesus Cristo, habitando com eles. Isto bastou para Filipe que era discípulo de Jesus. E eu creio que isto também será suficiente para você como o foi para mim.<br>
<br>
Está escrito em Mateus 13:11:<br>
<br>
“Porque a vós vos é dado a conhecer os mistérios do reino dos céus, mas a eles não lhes é dado.”<br>
<br>
Mateus 13:11<br>
<br>
Alguém pode tomar esses versículos e dizer que isto é se referindo aos judeus, mas a Palavra tem o seu exercício em seu dia, no seu determinado dia. Deus está parado com os judeus. Inconscientemente eles estão aguardando o Evangelho voltar para eles. O que vemos é a Escritura se repetindo. A filha de Herodias, representada pelo sistema denominacional dançando frente ao rei, procurando agradá-lo e tomando conselho com sua mãe, contra o profeta.<br>

Deus precisa de homens que queiram sofrer pelo Seu Nome, não pelo nome “Trindade”. O que tem Roma de Deus? E no entanto os protestantes estão unidos com ela através da doutrina da Trindade.<br>

Olhe para o Pai como Espírito. Como Espírito que não se vê, e então o verás em Jesus Cristo, o Filho Amado, “Quem vê a mim, vê ao Pai.” - João 14:9<br>

Veja o relato que transcrevemos do livro: “O Apóstolo dos Pés Sangrentos” de autoria do reverendo Boanerges Ribeiro, sobre uma experiência de Sadu Sundar Sing. Não é uma teoria, mas uma experiência daquele grande homem que imitou a Cristo em tudo. Sadu disse assim:<br>

“Houve um tempo que a doutrina da Trindade me deixava confuso. Pensava em três pessoas assentadas em três tronos. Mas tudo se esclareceu numa visão... Ali vi Cristo com seu glorioso corpo espiritual, assentado no trono. Onde quer que fosse, sempre O via. Cristo estava sempre no centro - figura que palavras não descrevem. Rosto brilhante como o sol, mas não ofuscante e amigo que com facilidade eu O contemplava. Sorria sempre com sorriso de afeto glorioso. Quando O vi pela primeira vez, tive a impressão que entre nós existia uma antiga união;<br>

Era como se Ele dissesse sem palavras: ‘Fui eu quem te criou.’ Senti com maior intensidade o que me sobreveio quando contemplei meu pai, após vários anos de separação. O velho afeto voltou; senti que fora dele anteriormente. Quando pela primeira vez entrei no céu olhei em torno e perguntei: mas onde está Deus? E alguém respondeu: como na terra aqui também Deus não é visto, porque é infinito. Mas aqui está Cristo, e Cristo é Deus; é a imagem do Deus invisível e somente n'Ele podemos ver Deus, tanto no céu como na terra.”<br>

Livro: O Apóstolo dos Pés Sangrentos - página 109<br>

