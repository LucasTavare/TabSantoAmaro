<b>• A Palavra vem ao profeta</b>
<br>
<br>
“Certamente o Senhor Jeová não fará coisa alguma, sem ter revelado o seu segredo a seus servos, os profetas.”<br>
<br>
Amós 3:7<br>
<br>
Através de toda a história registrada nas Escrituras, Deus sempre comunicou Sua Palavra por meio de profetas. Enoque, um profeta do período antediluviano, previu eventos futuros, incluindo a segunda vinda de Cristo quando ainda faltavam vários séculos para a primeira acontecer. Após Enoque, depois de três gerações, Deus levantou Noé para construir a arca e proclamar o iminente dilúvio que viria sobre o mundo que estava em terrível situação espiritual. Quando chegou o momento de libertar Israel do jugo egípcio, Deus escolheu Moisés como Seu instrumento e transmitiu Sua Palavra através dele. Ele era a boca de Deus para o povo. Posteriormente, à medida que Israel se estabelecia na Terra Prometida, Deus enviou profetas como Samuel, que atuou como o último dos juízes e ungiu o primeiro rei de Israel.<br>

Outros profetas notáveis, como Elias, Isaías, Jeremias, Amós e Daniel, também receberam a Palavra de Deus para suas respectivas eras e falaram em nome do Senhor. Este padrão de comunicação divina por meio de profetas é uma constante estabelecida pelas Escrituras para guiar e instruir o Seu povo ao longo da história:<br>

“E todo o povo viu os trovões e os relâmpagos e o sonido da buzina, e o monte fumegando; e o povo vendo isto, retirou-se e pôs-se de longe. E disseram a Moisés: Fala tu conosco e ouviremos; e não fale Deus conosco para que não morramos.”<br>
<br>
Êxodo 20:18-19<br>
<br>
Por meio das Escrituras, podemos compreender que o povo não suportava ouvir diretamente a voz de Deus. Por isso, pediram a Moisés que atuasse como mediador, recebendo as Palavras que Deus tinha para eles. Essa atitude do povo foi aprovada por Deus, doravante Ele sempre falaria por meio de profetas.<br>
<br>
- O Espírito de Elias vem cinco vezes<br>
Nas Escrituras, vemos que após o ministério de Elias, o tisbita, o Espírito que estava nele repousou sobre outros servos de Deus.<br>

Logo, a segunda manisfestação veio em porção dobrada sobre Eliseu, conforme mencionado em II Reis 2:15.<br>

A terceira, sobre João Batista, conforme mencionado em Lucas 1:17.<br>

A quarta, haveria de vir sobre outro profeta antes da grande tribulação.<br>

Por último, a quinta, viria sobre uma das testemunhas conforme prometido em Apocalipse 11.<br>

Isso resulta em cinco momentos distintos em que o Espírito que estava sobre Elias, o Espírito do próprio Deus, se faz presente na Terra. No entanto, é crucial ressaltar que isso não se refere à reencarnação, mas sim ao constante propósito de Deus de manifestar o ministério de Elias na história.<br>
<br>
- O precursor da Primeira Vinda<br>
Essa dinâmica persistiu ao longo da história. Cada profeta de Deus trouxe a porção da Palavra para seu respectivo período, até que João Batista, o último dos profetas do Antigo Testamento, apresentou Cristo, que era a Palavra feito carne e habitando entre nós.<br>

Quando João Batista apareceu, os judeus estavam esperando a vinda de Elias, curiosos, eles lhe perguntaram: “És tu Elias?” E sua resposta foi simples: “Não sou”:<br>

E perguntaram-lhe: Então quê? És tu Elias? E disse: Não sou. És tu profeta? E respondeu: Não.<br>

Disseram-lhe pois: Quem és? para que demos resposta àqueles que nos enviaram; que dizes de ti mesmo?<br>

Disse: Eu sou a voz do que clama no deserto: Endireitai o caminho do Senhor, como disse o profeta Isaías.”<br>
<br>
João 1:21-23<br>
<br>
Na realidade, ele não era o Elias que esperavam, aquele profetizado para vir antes do grande e terrível Dia do Senhor, conforme Malaquias 4. No entanto, ele era o Elias mencionado em Malaquias 3. Note que, embora o anjo tenha ordenado que ele se chamasse João, é notável que o próprio Jesus o tenha reconhecido como Elias, como vemos em Lucas 1:13:<br>

“Mas o anjo lhe disse: “Não tenha medo, Zacarias; sua oração foi ouvida. Isabel, sua mulher, lhe dará um filho, e você lhe dará o nome de João.”<br>
<br>
Lucas 1:13<br>
<br>
Embora João Batista tenha tido todas as características descritas nas Escrituras, os judeus não o puderam reconher como o homem que viria com o Espírito e Virtude de Elias, simplismente pelo fato de se apresentar com outro nome.<br>

João Batista desempenhou o papel de precursor, apresentando o Messias a Israel. No entanto, os líderes religiosos daquela época não o reconheceram, ao invés disso, fizeram dele tudo que quiseram, rejeitando sua Mensagem e seu ministério. Mesmo ansiando pela chegada desse profeta precursor, quando finalmente surgiu, foi rejeitado, preferiram suas próprias interpretações e tradições do que a verdadeira Palavra que abreria caminho para a primeira vinda de Jesus Cristo.<br>

Em Lucas 7:27, o próprio Jesus o identificou como o profeta anunciado em Malaquias 3:<br>

“Este é aquele de quem está escrito: Eis que envio o meu anjo diante da tua face, O qual preparará diante de ti o teu caminho.”<br>

“E eu vos digo que, entre os nascidos de mulheres, não há maior profeta do que João o Batista; mas o menor no reino de Deus é maior do que ele.”<br>
<br>
Lucas 7:27-28<br>
<br>
A mensagem de João não se alinhava com as crenças religiosas predominantes, pois os profetas não eram enviados para endossar organizações, mas para proclamar a Palavra de Deus. João Batista não fazia parte de nenhuma delas, como dos fariseus e saduceus, pois Deus geralmente escolhe indivíduos separados de tais estruturas para serem Seus profetas. Deus chama homens de onde deseja e os envia com Sua Palavra, como fez com Elias, cuja genealogia e antecedentes são desconhecidos, exceto por seu nome como “tisbita”. Isso tem ocorrido ao longo da história, quando o mensageiro aparece, ele é frequentemente rejeitado por aqueles que estão profundamente arraigados em suas crenças e tradições religiosas.<br>
<br>
- Um Elias do passado e outro do futuro<br>
O mesmo que se pode dizer acerca do Elias anunciado para este tempo do fim. Malaquias disse que Elias converteria o coração dos pais aos filhos e o coração dos filhos aos pais. Em verdade aqui estamos em volta de um duplo ministério. Porque sabemos que João Batista converteu o coração dos pais aos filhos, pois assim disse o anjo a Zacarias:<br>

E converterá muitos dos filhos de Israel ao SENHOR seu Deus,<br>

E irá adiante dele no espírito e virtude de Elias, para converter os corações dos pais aos filhos, e os rebeldes à prudência dos justos, com o fim de preparar ao Senhor um povo bem disposto.”<br>
<br>
Lucas 1:16-17<br>
<br>
Porém o Anjo não disse a Zacarias que João converteria o coração dos filhos aos pais, porque isso se referia ao Elias que haveria de vir antes da Segunda Vinda do Senhor. Quando o Anjo falava a Zacarias, citando a profecia de Malaquias, mencionou somente a parte que correspondia ao ministério de João onde diz: “... converter os corações dos pais aos filhos”, e ali se deteve, porque a outra parte pertencia ao Elias que viria como precursor da segunda vinda de Cristo. Note as palavras de Jesus em Mateus 17:10-12:<br>

“E os seus discípulos o interrogaram, dizendo: Por que dizem então os escribas que é mister que Elias venha primeiro?<br>

E Jesus, respondendo, disse-lhes: Em verdade Elias virá primeiro, e restaurará todas as coisas;<br>

Mas digo-vos que Elias já veio, e não o conheceram, mas fizeram-lhe tudo o que quiseram. Assim farão eles também padecer o Filho do homem.”<br>
<br>
Mateus 17:10-12<br>
<br>
Nesta porção das Escrituras, observamos que Jesus faz uso de dois tempos gramaticais ao referir-se a Elias: um que já veio (passado), que foi João Batista, e outro que estava por vir (futuro) para restaurar todas as coisas. É importante ressaltar que o Senhor não está procurando alterar a gramática, nem tão pouco brincando com as palavras; Ele simplesmente está descrevendo a vinda de dois Elias: um no futuro que “virá primeiro e restaurará todas as coisas” e outro no passado de quem “já veio e não o reconheceram.”<br>

A primeira parte desse ministério já havia sido realizada por João Batista: “converterá o coração dos pais aos filhos”. João preparou o coração dos pais da igreja primitiva para que pudessem receber Cristo, a Palavra. Para a segunda parte desse ministério, não há dúvida de que deveria vir um com Espírito de Elias antes da Grande Tribulação, do Grande e Terrível Dia do Senhor, pois havia uma obra específica a ser cumprida: “converterá o coração dos filhos aos pais”, ou seja, teria a missão de converter os filhos dos últimos dias à fé dos pais do dia de Pentecostes; de volta à fé primitiva dos apóstolos e profetas, preparando-os para a Vinda do Senhor. Portanto, este Elias não pode ser João Batista. Assim, em um único versículo das Escrituras, encontramos dois ministérios com uma diferença de centenas de anos entre eles.<br>

Como exemplo disso temos a mensagem que o Senhor deu na Sinagoga de Nazaré:<br>

E, chegando a Nazaré, onde fora criado, entrou num dia de sábado, segundo o seu costume, na sinagoga, e levantou-se para ler.<br>

E foi-lhe dado o livro do profeta Isaías; e, quando abriu o livro, achou o lugar em que estava escrito:<br>

O Espírito do Senhor é sobre mim, Pois que me ungiu para evangelizar os pobres. Enviou-me a curar os quebrantados de coração,<br>

A pregar liberdade aos cativos, E restauração da vista aos cegos, A pôr em liberdade os oprimidos, A anunciar o ano aceitável do Senhor.<br>

E, cerrando o livro, e tornando-o a dar ao ministro, assentou-se; e os olhos de todos na sinagoga estavam fitos nele.<br>

Então começou a dizer-lhes: Hoje se cumpriu esta Escritura em vossos ouvidos.<br>
<br>
Lucas 4:16-21<br>
<br>
O rolo que foi entregue a Jesus para ser lido corresponde a Isaías 61. Ele parou depois de ler o primeiro versículo e parte do segundo que correspondia com Seu ministério. Isso evidencia que Jesus não leu a segunda parte do versículo 2 de Isaías 61 que se refere ao “Dia da Vingança do nosso Deus”, porque em verdade faltavam muitos anos para a chegada do profeta que anunciaria esse dia, que é o Grande e Terrível Dia do Senhor, a Grande Tribulação.<br>

Isso ilustra como um único trecho das Escrituras pode conter eventos com anos de intervalo entre eles. No entanto, aqueles que não têm a revelação divina ficam confusos porque dependem do raciocínio humano, impedindo que compreendam os mistérios que Deus inseriu em Sua Palavra.<br>
<br>
- O precursor da Segunda Vinda<br>
A mente humana pode enxergar apenas um Elias, mas Jesus fala de um Elias no passado e de outro no futuro. O Elias desta era restaurará todas as coisas antes da Vinda do Senhor, e isso está perfeitamente alinhado com o que Pedro declarou sob a inspiração do Espírito Santo:<br>

E envie ele a Jesus Cristo, que já dantes vos foi pregado.<br>

O qual convém que o céu contenha até aos tempos da restauração de tudo, dos quais Deus falou pela boca de todos os seus santos profetas, desde o princípio.<br>
<br>
Atos 3:20,21<br>
<br>
Icone de Interrogacao<br>
Em outras palavras Jesus não virá sem que antes venha o tempo da restauração de todas as coisas. Porém, quem trará esta restauração?<br>

O próprio Senhor respondeu esta pergunta:<br>

E Jesus, respondendo, disse-lhes: Em verdade Elias virá primeiro, e restaurará todas as coisas;<br>
<br>
Mateus 17:11<br>
<br>
Esta restauração constitui um sinal marcante que nos permite compreender a época em que vivemos e saber quão perto estamos da vinda do Senhor, conforme as Escrituras afirmam:<br>

“O qual convém que o céu contenha até aos tempos da restauração de tudo, dos quais Deus falou pela boca de todos os seus santos profetas, desde o princípio.”<br>
<br>
Atos 3:21<br>
<br>
A história se repete, evidenciando a constância de Deus em Sua maneira de operar. Ao longo dos tempos, Ele tem enviado Sua Palavra por meio de profetas, mas os arraigados em crenças e tradições antigas frequentemente os rejeitam com suas mensagens. No entanto, Deus permanece inabalável em Seu plano, pois essa é a Sua escolhida forma de comunicar com Seu povo.<br>

Agora, conforme nos aproximamos do fim da última era da igreja, observamos a repetição desse padrão. Como foi no fim da dispensação judaica, onde Deus enviou um profeta preparando o caminho com sua mensagem, mas lamentavelmente, os líderes religiosos rejeitaram, Deus, para o fim da dispensação gentílica tem enviado um profeta com uma Mensagem poderosa, que tem agido como um machado à raiz das árvores. Desta vez com o alarido, preparando o povo do Senhor para a segunda vinda de Cristo, o arrebatamento. Deus segue agindo dessa forma porque Ele é imutável.<br>

O mundo religioso a tem desprezado, mas os eleitos, a semente predestinada a crer nessa Mensagem, a tem recebido com gratidão e está se nutrindo Dela.<br>

O grande e terrível dia do Senhor se aproxima rapidamente, como um fogo abrasador, onde aqueles que praticam o mal serão consumidos como a palha. Antes dessa grande tribulação, Deus anunciou que enviaria o profeta Elias para preparar o povo, que através de sua Mensagem, seria libertado desse iminente juízo:<br>

Aqueles que leem as Escrituras com revelação divina podem identificar o momento em que vivemos, pois muitos dos eventos previstos para este período já se desenrolam diante de nossos olhos. Hoje, vemos Israel firme em sua terra, exibindo força como nação, mas também presenciamos a disseminação da maldade em todas as esferas da vida humana. A ciência se expande em múltiplos aspectos, e a frieza nas igrejas é inegável, transformando-se em meros clubes religiosos, enquanto os autointitulados cristãos frequentemente se conformam com os costumes mundanos invadindo suas organizações religiosas. Muitas delas, hoje, se transformaram em negócios lucrativos. Essa é a apostasia dos últimos tempos. É evidente que o estado do mundo e da igreja indica a proximidade do juízo de Deus, a grande tribulação. Quem não vê é porque fechou os olhos para a realidade.<br>

De todos esses sinais, o mais significativo que mostra que estamos no tempo do fim é a presença do profeta que Deus prometeu enviar antes desse juízo final. Esse profeta restaurará todas as verdades de Deus que os homens perverteram ao longo dos séculos. O mesmo Jesus que disse que o Elias daquele tempo, João Batista, já havia vindo, também disse que haveria um Elias que viria para restaurar todas as coisas. Esse é o mesmo Elias profetizado na segunda parte do verso 6 de Malaquias 4:<br>

E ele converterá o coração dos pais aos filhos, e o coração dos filhos a seus pais...”<br>

“Eis que vos envio o profeta Elias antes que venha o dia grande e terrível do Senhor.”<br>
<br>
Malaquias 4:5-6<br>
<br>
Antes da Vinda de Cristo para o arrebatamento de Seu povo, tinha que vir um mensageiro com o Espírito e Virtude de Elias, para preparar o povo tirando-o de todos os horrores denominacionais, e separando-o de todos os costumes e tradições contrárias à Palavra de Deus. Esta é a conversão dos filhos à fé dos pais, o que significa deixar todo costume e tradição que têm sido acrescentados à fé e prática dos apóstolos. O mensageiro desta era tinha que ser um profeta. Ele tinha que exercer um ministério profético baseado somente na Palavra de Deus. Este profeta do fim é o anjo à sétima e última era da igreja. E sua mensagem é a revelação que faz manifesto os segredos de Deus:<br>

Mas nos dias da voz do sétimo anjo, quando tocar a sua trombeta, se cumprirá o segredo de Deus, como anunciou aos profetas, seus servos.<br>
<br>
Apocalipse 10:7<br>
<br>
Esta é uma profecia já cumprida! Se deu através de um simples homem, William Marrion Branham, que carinhosamente é chamado de Ir. Branham. Na manhã de seu nascimento, em 06 de abril de 1909, uma Luz entrou e pairou sobre ele no quarto, numa humilde cabana a leste de Kentucky - EUA, dando início a uma vida sobrenatural que mudaria a face do moderno mundo cristão.<br>

Em 11 de junho de 1933, enquanto batizava a margem do Rio Ohio em Jeffersonville – Indiana - EUA, diante de uma multidão, do céu desceu uma Luz na forma de uma Bola de Fogo que lhe disse: “Assim como João Batista foi enviado para ser o precursor da Primeira Vinda de Cristo, sua Mensagem precursará Sua Segunda Vinda”.<br>

Em 1947, o Anjo de Deus lhe apareceu, enquanto orava em uma caverna, e o comissionou dizendo que grandes sinais seguiria seu ministério, e ele oraria por reis, monarcas e demais. Ele pensou, sendo um homem pobre e sem educação, como seria capaz de fazer essa obra. O Anjo lhe disse que, como a Moisés foram dados dois sinais para confirmar seu ministério, a ele também seria: um para orar pelos enfermos (aparecia em sua mão esquerda) e o outro para discernir os segredos dos corações (Ele era capaz de dizer os pecados que estavam ocultos nas vidas das pessoas).<br>

William Branham teve o mais notável ministério que outrora já ouve, pois os mistérios ocultos de Deus escondidos na Santa Palavra têm sido revelados, tais como “Os Sete Selos” de Apocalipse 6, 7 e 8 . Manifestando o Poder de Deus, sua Mensagem marcou o maior derramamento do Espírito Santo desde o dia de Pentecostes. Centenas participaram de suas campanhas. Não havia demônio de enfermidade frente a ele que em Nome do Senhor Jesus Cristo não era expulso. Cumprindo aquilo Jesus disse em João 14:14: “Na verdade, na verdade vos digo que aquele que crê em mim também fará as obras que eu faço, e as fará maiores do que estas, porque eu vou para meu Pai.”. Outros evangelistas como, Oral Roberts, T. L. Osborn, A. A. Allen e outros, inspirados por seu ministério, começaram seus próprios reavivamentos de cura. O Senhor fez chover suas bênçãos como nunca antes. A mão de cura de Jesus Cristo tinha mais uma vez tocado o Seu povo.<br>

Este profeta foi enviado por Deus para a presente era e recebeu a divina ordenação de pregar a pura Palavra de Deus, exatamente como foi proferida pelos profetas e apóstolos. Sua missão principal tem sido preparar o povo de Deus, cumprindo assim a parte mais crucial de seu ministério: guiar os predestinados de volta à verdadeira Palavra de Deus. Isso tem sido claramente sucedido nesta era.<br>

Através do ministério do profeta desta era, William Branham, os filhos têm se convertido à fé genuína dos apóstolos, cumprindo a segunda parte de Malaquias 4:6, resultando na restauração da Palavra de Deus à sua pureza original. Esse ministério também trouxe à luz a falsidade das crenças religiosas e revelou a verdadeira condição das igrejas que se autodenominam cristãs. O irmão Branham desafiou líderes religiosos em várias ocasiões, convidando-os a demonstrar suas supérfluas práticas religiosas diante do povo. Em cada ocasião, o poder de Deus se manifestou de maneira impressionante diante de milhares de pessoas.<br>

Embora muitos não tenham conhecimento desses fatos, isto não anula sua veracidade; assim como a verdade de Deus prevalece, mesmo quando muitos desconhecem os milagres e o conteúdo da Bíblia.<br>

Nas Sagradas Escrituras, tanto no Velho como no Novo Testamento, vemos claramente os sinais que nos indicam que temos chegado ao fim, pois resistem hoje a Mensagem e o profeta exatamente como Janes e Jambres resistiram a Moisés:<br>

“Homens de dura cerviz, e incircuncisos de coração e ouvido, vós sempre resistis ao Espírito Santo; assim vós sois como vossos pais.<br>

A qual dos profetas não perseguiram vossos pais? Até mataram os que anteriormente anunciaram a vinda do Justo, do qual vós agora fostes traidores e homicidas;”<br>
<br>
Atos 7:51-52<br>
<br>
O irmão Branham denunciou veementemente a imoralidade da mulher, que se tornou um foco central de corrupção nesta geração.<br>

Assim como no tempo de João, os judeus esperavam a vinda de Elias, mas não o reconheceram como profeta, mesmo que sua validade fosse claramente confirmada pelas Escrituras. Suas interpretações errôneas os impediram de aceitar tanto João quanto Cristo. Da mesma forma, nos dias atuais, a história se repete, pois as Escrituras apontam para a vinda de um mensageiro antes da grande tribulação, designado para preparar o povo do Senhor para o arrebatamento. Deus fielmente cumpriu Sua promessa, enviando o profeta desta era. No entanto, o mundo religioso contemporâneo tem recusado recebê-lo. No entanto, assim como no passado, existem aqueles predestinados por Deus para ouvir Sua voz por meio de Seu profeta, e estão se alimentando e se preparando por meio de sua mensagem.<br>

João Batista não tinha seu nome escrito no Velho Testamento confirmando que ele era o Elias precursor da Primeira Vinda de Cristo, daa mesma forma, William Branham não tem seu nome escrito no Novo Testamento, mas sua vida e sua Mensagem demonstra que ele é a quarta manifestação do Espírito de Elias, preparando o caminho para a Segunda Vinda de Cristo, que ocorre antes da Grande Tribulação.<br>

De modo notável, como os escribas registraram os sermões dos profetas bíblicos, seus sermões foram registrados em fita magnética e posteriormente transcritos em livros e pela a internet.<br>
