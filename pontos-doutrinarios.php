- Pergunta sobre João 14:23<br>
<br>
São João 14:23 não distingue o Pai do Filho? “Se alguém me ama guardará a Minha Palavra e o meu Pai o amará e viremos e faremos nele morada.”<br>

Não pense em três pessoas, mas em três ofícios. Se você crê que Deus é Onipotente, então crerá também que Ele pode se manifestar em vários modos, várias formas ao mesmo tempo. Então pense em três manifestações: Rei, Sacerdote e Profeta.<br>

Como poderia Deus sendo uma pessoa juntar-se a outra pessoa para fazer morada em seu corpo, sendo você uma outra pessoa? Nesse caso você teria duas pessoas morando em seu corpo? E a terceira?<br>

Aí Jesus está falando Dele e de Seu Pai. O Espírito que faz morada no homem é o Espírito Santo:<br>

“Não sabeis vós que sois o templo de Deus e que o Espírito de Deus habita em vós?”<br>
<br>
I Coríntios 3:16<br>
<br>
Este Espírito exerce os três ofícios sobre o homem: Reina, Intercede e Ilumina. Isto é, exorta, edifica e consola.<br>

Você não tem três pessoas em seu corpo:<br>

“Jesus respondeu, e disse-lhe: Se alguém me ama, guardará a minha palavra, e meu Pai o amará, e viremos para ele, e faremos nele morada.”<br>
<br>
João 14:23<br>
<br>
Será que o Pai deixou o trono? O Filho deixou a intercessão? É o Espírito de Verdade que o mundo não pode conhecer, reinando, intercedendo e consolando. Por isso está no plural. Deus em Seus ofícios. É Deus sobre nós, Deus por nós e Deus em nós.<br>

Você não tem três pessoas em seu corpo.<br>

“Um só Deus e Pai de todos, o qual é sobre todos, e por todos e em todos vós.”<br>
<br>
Efésios 4:6<br>
<br>
- Pergunta sobre João 15:26<br>
<br>
Mas como se explica então João 15:26, onde diz: “Mas quando vier o Consolador, que eu da parte do Pai vos hei de enviar, aquele Espírito de Verdade que procede do Pai, Ele testificará de mim.”<br>

Pois bem, vamos começar por aqui: “Espírito de Verdade”. Perguntamos: Espírito é pessoa? A Bíblia diz que não. Jesus disse:<br>

“Vede as minhas mãos e os meus pés, que sou eu mesmo; apalpai-me e vede, pois um espírito não tem carne e nem ossos, como vedes que eu tenho.”<br>
<br>
Lucas 24:39<br>
<br>
Agora, para melhor esclarecer, vamos perguntar:<br>
<br>
Quem é o Pai de Jesus? A própria Bíblia tem resposta para as perguntas bíblicas:<br>

“Achou-se ter concebido do Espírito Santo.”<br>
<br>
Mateus 1:18<br>
<br>
Aquele de quem a mulher concebe, é logicamente o pai da criança. Então, se Maria concebeu do Espírito Santo, conforme Mateus 1:18, está claro que o Pai de Jesus é o Espírito Santo. Mas, que Espírito é esse? É Deus. Jesus disse:<br>

“Deus é Espírito, e importa que os que o adoram o adorem em espírito e em verdade.”<br>
<br>
João 4:24<br>
<br>
O Espírito Santo veio no Pentecostes e se repartiu em cada um para que pudesse cumprir o que disse Jesus:<br>

“Naquele dia vós conhecereis que eu estou em meu Pai, e vós em mim, e eu em vós”<br>
<br>
João 14:20<br>
<br>
O Deus Todo Poderoso tem vindo como Espírito para encher Sua verdadeira Igreja.<br>

Vê você? Onde estão pois as três pessoas? Essa doutrina da Trindade foi elaborada no ano 325 d.C. sob o papa São Silvestre e confirmada como dogma no ano 381 d.C. pelo papa São Dâmaso. Onde a Bíblia cita o nome Trindade? Onde? Se isto fosse de Deus estaria em Sua Palavra.<br>
